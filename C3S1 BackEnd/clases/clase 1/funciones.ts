const sumandoPrimo = (a: number, b: number) =>
{
    return a+b;
};

const sumandoPrimoLento = (a: any, b: number) =>
{
    return a+b;
};

const sumandoPrimoTonto = (a: unknown, b: number) =>
{
    return a+b;
};


console.log(sumandoPrimo(5,4));
console.log(sumandoPrimoLento(5, 4));
console.log(sumandoPrimoTonto(5,4));
